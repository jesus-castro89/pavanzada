package org.brick_breaker.utils.collisions;

public enum EdgeType {

    /**
     * Tipo de borde que representa la parte superior de un sprite.
     */
    LEFT_EDGE,
    /**
     * Tipo de borde que representa la parte inferior de un sprite.
     */
    RIGHT_EDGE,
    /**
     * Tipo de borde que representa la parte izquierda de un sprite.
     */
    TOP_EDGE,
    /**
     * Tipo de borde que representa la parte derecha de un sprite.
     */
    BOTTOM_EDGE
}
